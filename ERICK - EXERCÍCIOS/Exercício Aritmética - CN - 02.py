# Faça um algoritmo para ler três números e imprimir
# a soma, mídia e produto dos números lidos.

a = int(input("Escreva o primeiro número:"))
b = int(input("Escreva o segundo número:"))
c = int(input("Escreva o terceiro número:"))
print(f'A soma desses números é igual a {a + b + c}')
print(f'A média desses números é igual a {int((a + b + c)/3)}')
print(f'O produto desses números é igual a {a * b * c}')
